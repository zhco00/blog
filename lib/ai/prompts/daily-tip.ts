/**
 * Extracts metadata from generated post content
 */
export function extractTipMetadata(content: string): {
  title: string
  summary: string
} {
  const titleMatch = content.match(/^#\s+(.+)$/m)
  const title = titleMatch ? titleMatch[1] : 'Daily Post'

  const summaryMatch = content.match(/##\s+핵심 요약\s+([\s\S]+?)(?=\n##)/)
  const bgMatch = content.match(/##\s+배경\s+([\s\S]+?)(?=\n##)/)
  const summary = summaryMatch
    ? summaryMatch[1].trim().replace(/\n/g, ' ').replace(/- /g, '').slice(0, 200)
    : bgMatch
      ? bgMatch[1].trim().replace(/\n/g, ' ').slice(0, 200)
      : ''

  return { title, summary }
}
